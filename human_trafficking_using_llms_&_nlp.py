EvalResult = namedtuple(

    "EvalResult",

    [
        "loss",
        "acc",
        "f1",
        "labels",
        "preds"
    ]
)

# ------------------------------------------------------------
# Evaluation function
# ------------------------------------------------------------
def evaluate(loader, threshold=0.35):

    # --------------------------------------------------------
    # Switch model to evaluation mode
    # --------------------------------------------------------
    model.eval()

    # --------------------------------------------------------
    # Store predictions
# --------------------------------------------------------
    all_preds = []

    # --------------------------------------------------------
    # Store labels
    # --------------------------------------------------------
    all_labels = []

    # --------------------------------------------------------
    # Accumulate loss
    # --------------------------------------------------------
    total_loss = 0

    # --------------------------------------------------------
    # Disable gradients
    # --------------------------------------------------------
    with torch.no_grad():

        # ----------------------------------------------------
        # Iterate through batches
        # ----------------------------------------------------
        for batch in loader:

            # ------------------------------------------------
            # Move tensors to device
            # ------------------------------------------------
            batch = {
                k: v.to(device)
                for k, v in batch.items()
            }

            # ------------------------------------------------
            # Forward pass
            # ------------------------------------------------
            outputs = model(**batch)

            # ------------------------------------------------
            # Raw prediction scores
            # ------------------------------------------------
            logits = outputs.logits

            # ------------------------------------------------
            # Compute weighted loss
            # ------------------------------------------------
            loss = loss_fn(
                logits,
                batch["labels"]
            )

            # ------------------------------------------------
            # Add batch loss
            # ------------------------------------------------
            total_loss += loss.item()

            # ------------------------------------------------
            # Convert logits to probabilities
            # ------------------------------------------------
            probs = torch.softmax(
                logits,
                dim=1
            )

            # ------------------------------------------------
            # Custom threshold
            # ------------------------------------------------
            preds = (
                probs[:, 1] >= threshold
            ).long()

            # ------------------------------------------------
            # Save predictions
            # ------------------------------------------------
            all_preds.extend(
                preds.cpu().numpy()
            )

            # ------------------------------------------------
            # Save labels
            # ------------------------------------------------
            all_labels.extend(
                batch["labels"]
                .cpu()
                .numpy()
            )

    # --------------------------------------------------------
    # Compute accuracy
    # --------------------------------------------------------
    acc = accuracy_score(
        all_labels,
        all_preds
    )

    # --------------------------------------------------------
    # Compute macro F1
    # --------------------------------------------------------
    f1 = f1_score(
        all_labels,
        all_preds,
        average="macro"
    )

    # --------------------------------------------------------
    # Return structured results
    # --------------------------------------------------------
    return EvalResult(

        loss=total_loss / len(loader),

        acc=acc,

        f1=f1,

        labels=all_labels,

        preds=all_preds
    )

