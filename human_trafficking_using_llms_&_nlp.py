# ------------------------------------------------------------
# Predict single unseen text
# ------------------------------------------------------------
def predict(text, threshold=0.35):

    # --------------------------------------------------------
    # Evaluation mode
    # --------------------------------------------------------
    model.eval()

    # --------------------------------------------------------
    # Clean input text
    # --------------------------------------------------------
    cleaned = clean_text(text)

    # --------------------------------------------------------
    # Tokenize input
    # --------------------------------------------------------
    encoding = tokenizer(

        cleaned,

        truncation=True,

        padding="max_length",

        max_length=MAX_LEN,

        return_tensors="pt"
    )

    # --------------------------------------------------------
    # Move tensors to device
    # --------------------------------------------------------
    encoding = {
        k: v.to(device)
        for k, v in encoding.items()
    }

    # --------------------------------------------------------
    # Disable gradients
    # --------------------------------------------------------
    with torch.no_grad():

        # ----------------------------------------------------
        # Forward pass
        # ----------------------------------------------------
        logits = model(**encoding).logits

        # ----------------------------------------------------
        # Convert logits to probabilities
        # ----------------------------------------------------
        probs = torch.softmax(
            logits,
            dim=1
        )

        # ----------------------------------------------------
        # Probability of trafficking class
        # ----------------------------------------------------
        trafficking_prob = probs[
            0, 1
        ].item()

    # --------------------------------------------------------
    # Final prediction using threshold
    # --------------------------------------------------------
    prediction = (

        "Trafficking"

        if trafficking_prob >= threshold

        else "Legitimate"
    )

    # --------------------------------------------------------
    # Return structured output
    # --------------------------------------------------------
    return {

        "probability": round(
            trafficking_prob,
            4
        ),

        "prediction": prediction
    }

