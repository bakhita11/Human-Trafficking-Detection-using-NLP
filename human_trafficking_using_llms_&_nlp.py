# ------------------------------------------------------------
# Data augmentation improves robustness
# ------------------------------------------------------------
def augment_text(text):

    # --------------------------------------------------------
    # Split text into words
    # --------------------------------------------------------
    words = text.split()

    # --------------------------------------------------------
    # Random word dropout
    # --------------------------------------------------------
    words = [
        w for w in words
        if np.random.rand() > 0.1
    ]

    # --------------------------------------------------------
    # Random neighboring word swap
    # --------------------------------------------------------
    if len(words) > 4 and np.random.rand() < 0.3:

        # random index
        i = np.random.randint(
            0,
            len(words)-1
        )

        # swap neighboring words
        words[i], words[i+1] = (
            words[i+1],
            words[i]
        )

    # --------------------------------------------------------
    # Return modified sentence
    # --------------------------------------------------------
    return " ".join(words)

